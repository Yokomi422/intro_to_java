package ch01;
public class Math {
    public static void main(String args[]) {
        System.out.println(10 + 10);
        System.out.println(100 % 3);
        System.out.println("Hello" + " World");
        int a = 10;
        int b = a++;
        int c = ++a;
        System.out.println("a = " + a);
        System.out.println("b = " + b);
        System.out.println("c = " + c);
    }
} 